package objetos;

public class Conctac {
	
	private int vcodigoctac;
	private String vnombrectac;
	private String vnumeroctac;
	private String vancestroctac;
	private String vnumaltctac;
	private String vancaltctac;
	private int vcodigotcta;
	private int vcodigoesta;
	
	
	public Conctac(int vcodigoctac, String vnombrectac, String vnumeroctac, String vancestroctac, String vnumaltctac,
			String vancaltctac, int vcodigotcta, int vcodigoesta) {
		super();
		this.vcodigoctac = vcodigoctac;
		this.vnombrectac = vnombrectac;
		this.vnumeroctac = vnumeroctac;
		this.vancestroctac = vancestroctac;
		this.vnumaltctac = vnumaltctac;
		this.vancaltctac = vancaltctac;
		this.vcodigotcta = vcodigotcta;
		this.vcodigoesta = vcodigoesta;
	}
	
	public Conctac() {
		super();
	}



	public int getVcodigoctac() {
		return vcodigoctac;
	}
	public void setVcodigoctac(int vcodigoctac) {
		this.vcodigoctac = vcodigoctac;
	}
	public String getVnombrectac() {
		return vnombrectac;
	}
	public void setVnombrectac(String vnombrectac) {
		this.vnombrectac = vnombrectac;
	}
	public String getVnumeroctac() {
		return vnumeroctac;
	}
	public void setVnumeroctac(String vnumeroctac) {
		this.vnumeroctac = vnumeroctac;
	}
	public String getVancestroctac() {
		return vancestroctac;
	}
	public void setVancestroctac(String vancestroctac) {
		this.vancestroctac = vancestroctac;
	}
	public String getVnumaltctac() {
		return vnumaltctac;
	}
	public void setVnumaltctac(String vnumaltctac) {
		this.vnumaltctac = vnumaltctac;
	}
	public String getVancaltctac() {
		return vancaltctac;
	}
	public void setVancaltctac(String vancaltctac) {
		this.vancaltctac = vancaltctac;
	}
	public int getVcodigotcta() {
		return vcodigotcta;
	}
	public void setVcodigotcta(int vcodigotcta) {
		this.vcodigotcta = vcodigotcta;
	}
	public int getVcodigoesta() {
		return vcodigoesta;
	}
	public void setVcodigoesta(int vcodigoesta) {
		this.vcodigoesta = vcodigoesta;
	}
	
	
}
